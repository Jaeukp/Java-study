package ch01;


public class Variables {
    public static void main(String[] args) {
        // 자바에서 변수(Variables)란? 이름이 있는 메모리 공간.
        // 컴퓨터는 작업을 하려면 메모리(RAM)에 무엇이든 올라가 있어야 작업이 가능하다.
        // 프로그래밍이란? 언어를 컴퓨터 언어로 컴파일(번역&해석)하면서 작업하는것. 그래야 컴퓨터가 알아듣고 작업함.

        // 변수 선언(declare)이란? 이름이 있는 메모리 공간 만들기.
        int age; // int(integer:정수)는 메모리공간유형 age라는 변수이름에 정수 값을 저장할 수 있다.
        // 값 할당(assignment)
        age = 25; // 변수의 값을 저장

        int studentAge = 32867;

        System.out.println(studentAge);



    }
}
